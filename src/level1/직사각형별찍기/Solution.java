package level1.직사각형별찍기;

import java.util.Scanner;

public class Solution {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int a = sc.nextInt();
		int b = sc.nextInt();
		
		//b만큼 반복하면서 a만큼 별찍기
		for(int i = 0; i<b; i++) {
			for(int j = 0; j<a; j++) {
				System.out.print("*");
			}
			System.out.println();
		}
		
	}
}
